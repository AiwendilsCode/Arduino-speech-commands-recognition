void minMaxNormal(double x[][NUM_INPUT], int length) // x[n] = (x[n] - min(x)) / (max(x) - min(x));
{
	double max = x[0][0];
	double min = x[0][0];
	
	for (int w = 0; w < length; w++)
	{
		for (int i = 1; i < NUM_INPUT; i++)
		{
			if (x[w][i] < min)
				min = x[w][i];
			else if (x[w][i] > max)
				max = x[w][i];
		}

		double range = max - min;

		if (range == 0.0)
		{
			for (int i = 0; i < NUM_INPUT; i++)
			{
				x[w][i] = 0.5;
			}
			return;
		}

		for (int i = 0; i < NUM_INPUT; i++)
		{
			x[w][i] = (x[w][i] - min) / range;
		}
	}
}

void hyperTanFunction(double source[], double dest[], int length)
{
	for (int i = 0; i < length; i++)
	{
		if (source[i] > 10.0)
			dest[i] = 1;
		else if (source[i] < -10.0)
			dest[i] = -1;
		else
			dest[i] = tanh(source[i]);
	}
}

void softMax(double source[], double dest[], int length) // x[n] = exp(x[n] - max(x)) / (exp(x[0] - max(x)) + exp(x[1] - max(x)) + ... + exp(x[y] - max(x)))
{
	double max = maxInArray(source, length);

	double scale = 0.0;

	for (int i = 0; i < length; i++)
		scale += exp(source[i] - max);

	for (int i = 0; i < length; i++)
		dest[i] = exp(source[i] - max) / scale;
}

double logSigmoid(double x)
{
	if (x > 30.0)
		return 1;
	else
		return 1 / (1 + exp(x));
}

int computeOutput(double input[NUM_INPUT])
{
	for (int l = 0; l < NUM_LAYERS; l++)
	{
		if (l < NUM_LAYERS - 1)
		{
			for (int h = 0; h < NUM_HIDDEN; h++)
			{
				if (l != 0)
				{
					for (int k = 0; k < NUM_HIDDEN; k++)
					{
						hiddenSums[l][h] += activatedHiddenSums[l - 1][k] * weights[l][h][k];
					}
					hiddenSums[l][h] += hiddenBiases[l][h];
				}
				else
				{
					for (int k = 0; k < NUM_INPUT; k++)
					{
						hiddenSums[l][h] += input[k] * weights[l][h][k];
					}
					hiddenSums[l][h] += hiddenBiases[l][h];
				}
			}

			hyperTanFunction(hiddenSums[l], activatedHiddenSums[l], NUM_HIDDEN);
		}
		else
		{
			for (int o = 0; o < NUM_OUTPUT; o++)
			{
				for (int h = 0; h < NUM_HIDDEN; h++)
				{
					outputSums[o] += activatedHiddenSums[l - 1][h] * weights[l][o][h];
				}
				outputSums[o] += outputBiases[o];
			}
			softMax(outputSums, activatedOutputSums, NUM_OUTPUT);
		}
	}

	return indexOfMaxInArray(activatedOutputSums, NUM_OUTPUT);
}
