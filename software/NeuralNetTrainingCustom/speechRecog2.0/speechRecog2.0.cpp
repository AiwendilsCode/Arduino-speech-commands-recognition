void updateWeights(double input[NUM_INPUT])
{
	double delta;

	for (int l = 0; l < NUM_LAYERS; l++)
	{
		if (l < NUM_LAYERS - 1)
		{
			for (int h = 0; h < NUM_HIDDEN; h++)
			{
				if (l != 0)
				{
					for (int k = 0; k < NUM_HIDDEN; k++)
					{
						delta = weightsGrads[l][h] * LEARNRATE * activatedHiddenSums[l - 1][k];
						weights[l][h][k] += (delta + prevWeightsDeltas[l][h][k] * MOMENTUM);
						prevWeightsDeltas[l][h][k] = delta;
					}
				}
				else
				{
					for (int k = 0; k < NUM_INPUT; k++)
					{
						delta = weightsGrads[l][h] * LEARNRATE * input[k];
						weights[l][h][k] += (delta + prevWeightsDeltas[l][h][k] * MOMENTUM);
						prevWeightsDeltas[l][h][k] = delta;
					}
				}

				delta = weightsGrads[l][h] * LEARNRATE;
				hiddenBiases[l][h] += (delta + prevHiddenBiasesDeltas[l][h] * MOMENTUM);
				prevHiddenBiasesDeltas[l][h] = delta;
			}
		}
		else
		{
			for (int o = 0; o < NUM_OUTPUT; o++)
			{
				for (int h = 0; h < NUM_HIDDEN; h++)
				{
					delta = weightsGrads[l][o] * LEARNRATE * activatedHiddenSums[NUM_LAYERS - 2][h];
					weights[l][o][h] += (delta + prevWeightsDeltas[l][o][h] * MOMENTUM);
					prevWeightsDeltas[l][o][h] = delta;
				}

				delta = weightsGrads[NUM_LAYERS - 1][o] * LEARNRATE;
				outputBiases[o] += delta + prevOutputBiasesDeltas[o] * MOMENTUM;
				prevOutputBiasesDeltas[o] = delta;
			}
		}
	}
}

void getGrads() // relu derivation is g(x) = 1, y >= 0  g(x) = 0, y < 0 and softmax derivation is y(1 - y)
{
	MSE = meanSquaredError(desValues);
	double derivative;
	double sum = 0;

	for (int i = 0; i < NUM_OUTPUT; i++)
	{
		derivative = softMaxDerivative(activatedOutputSums[i]);
		weightsGrads[NUM_LAYERS - 1][i] = derivative * (desValues[i] - activatedOutputSums[i]);
	}

	for (int l = NUM_LAYERS - 2; l > -1; l--)
	{
		for (int h = 0; h < NUM_HIDDEN; h++)
		{
			derivative = hyperTanDerivative(activatedHiddenSums[l][h]);

			if (l != NUM_LAYERS - 2)
			{
				for (int k = 0; k < NUM_HIDDEN; k++)
				{
					sum += weightsGrads[l + 1][k] * weights[l + 1][h][k];
				}
			}
			else
			{
				for (int k = 0; k < NUM_OUTPUT; k++)
				{
					sum += weightsGrads[l + 1][k] * weights[l + 1][k][h];
				}
			}

			weightsGrads[l][h] = sum * derivative;
			sum = 0;
		}
	}
}

void shuffle(int arr[], int length)
{
	srand(time(NULL));
	int temp;
	for (int i = 0; i < length; i++)
	{
		arr[i] = i;
	}

	for (int i = 0; i < length; i++)
	{
		int random = rand() % WORDS;
		temp = arr[i];
		arr[i] = arr[random];
		arr[random] = temp;
	}
}

