double CrossEnthropyError(double* predicted, double* desired)
{
	double sumCEE = 0.0;

	for (int i = 0; i < NUM_OUTPUT; i++)
	{
		sumCEE += desired[i] * log(1e-15 + predicted[i]);
	}

	return abs(sumCEE * (1.00 / NUM_OUTPUT));
}

